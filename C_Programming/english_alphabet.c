#include <stdio.h>

int main() {
    
	printf("English Alphabet: \n\n");
	
	for(char i = 'A'; i <= 'Z'; i++)
	    printf(" %c ", i);
	    
	printf("\n\n------------------------------------------------------------------------------\n\n");
	    
	for(int i = 'a'; i <= 'z'; i++)
	    printf(" %c ", i);
	    
	return 0;
}
