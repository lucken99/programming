//Control flow: if-else statement

#include <stdio.h>

int main() {

	int num = 30;	//Initialization

	if(num < 20) {	//Checking condition

		printf("%d is less than 20", num);	//Printing if condition is true
	}
	else {
		
		printf("%d is greater than 20", num);	//Printing if condition is false
	}

	return 0;
}

