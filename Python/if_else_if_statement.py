# Control flow: if else-if statement

number = 30
if number < 20:	# checking condition
	print(number ,"is less than 20")
	
elif number < 100:
	print(number ,"is between 20 and 100 inclusive")	# executes if, if condition is false
								# and elif condition is true
else:
	print(number ,"is greater than 100") 
	
